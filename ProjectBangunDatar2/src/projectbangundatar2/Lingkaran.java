public class Lingkaran {
    //luas
    double luas(int r){
        double hasil = 3.14 * r *r;
        return hasil;   
    }
    
    double luas(double r){
        double hasil = 3.14 * r *r;
        return hasil;   
    }
    
    //keliling
    double keliling(int r){
        double hasil = 2 * 3.14 * r;
        return hasil;
    }
    
    double keliling(double r){
        double hasil = 2 * 3.14 * r;
        return hasil;
    }
}
